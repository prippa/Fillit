#include "libft.h"

int				ft_nbrlen(int c)
{
	int			i;
	long long	a;

	i = 1;
	if (c < 0)
	{
		i++;
		a = -(long long)c;
	}
	else
		a = (long long)c;
	while (a > 9)
	{
		a /= 10;
		i++;
	}
	return (i);
}
